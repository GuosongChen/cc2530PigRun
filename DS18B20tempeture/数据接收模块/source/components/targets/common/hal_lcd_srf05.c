/*显示 5*7 点阵图像、ASCII, 或5x7 点阵的自造字符、其他图标*/
void display_graphic_5x7(uint16 page,uint8 column,uint8 *dp)
{
  uint16 col_cnt;
  uint8 page_address;
  uint8 column_address_L,column_address_H;
   page_address = 0xb0+page-1;
  lcd_cs1=0;
//  Rom_CS = 1; // Rick
  column_address_L =(column&0x0f)-1;
  column_address_H =((column>>4)&0x0f)+0x10;
  transfer_command_lcd(page_address); /*Set Page Address*/
  transfer_command_lcd(column_address_H); /*Set MSB of column Address*/
  transfer_command_lcd(column_address_L); /*Set LSB of column Address*/
  for (col_cnt=0;col_cnt<6;col_cnt++)
  {
    transfer_data_lcd(*dp);
    dp++;
  }
  lcd_cs1=1;
}

void LCD_write_char( unsigned char c)
  {
    unsigned char line;
	lcd_cs1=0;
    c -= 32;
    for (line=0; line<6; line++)
    transfer_data_lcd(font6x8[c][line]);
	lcd_cs1=1;
  }

void LCD_write_english_string(unsigned char X,unsigned char Y,char *s)
  {
    lcd_cs1=0;
	lcd_address(X,Y);
    while (*s) 
      {
	 LCD_write_char(*s);
	 s++;
      }
	lcd_cs1=1;
  }

void LCD_disp_char(unsigned char x, unsigned char y, unsigned char c)
{
 	unsigned char temp[3];
	lcd_cs1=0;
 	temp[0]='0'+c/100;
 	temp[1]='0'+(c-(c/100)*100)/10;
 	temp[2]='0'+c%10;
    lcd_address(x,y);
    LCD_write_char(temp[0]); 
    LCD_write_char(temp[1]);
    LCD_write_char(temp[2]);
	lcd_cs1=1;
}

void LCD_disp_int(unsigned char x, unsigned char y, unsigned int t)
{
 	unsigned char a[5];
        lcd_cs1=0;

	a[0]='0'+(t/10000)%10;         //取得整数值到数组         	
	a[1]='0'+(t/1000)%10;                                     	
	a[2]='0'+(t/100)%10;                                      	
	a[3]='0'+(t/10)%10;                                       	
	a[4]='0'+(t/1)%10; 
        lcd_address(x,y);
	LCD_write_char(a[0]); 
        LCD_write_char(a[1]);
        LCD_write_char(a[2]);
        LCD_write_char(a[3]);
        LCD_write_char(a[4]);
        lcd_cs1=1;
}

void LCD_disp_Float (unsigned char x, unsigned char y,float num,unsigned char wei)
{ 
  unsigned int intpart;
  float fracpart;
  char n=0,m=0;
  char numbuff[5];
  char temp;
  
  lcd_cs1=0;
  lcd_address(x,y);
  if (num==0) LCD_write_char('0');
  else
  {
    if (num<0)
    { 
      LCD_write_char('-');
      num=(0-num);
    }

   intpart=(int)num;
   fracpart=num-intpart;

   if (intpart==0) { 
   	LCD_write_char('0');
   }
   while(intpart!=0)
    {
      temp=intpart;
      intpart=intpart/10;
      numbuff[n]=temp-(((intpart<<2)+intpart)<<1)+48;
      n++;
    }
   while(n>0)
   {
     LCD_write_char(numbuff[n-1]); 
     n--;
   }

   if (fracpart!=0)
   { 
     LCD_write_char('.');
     while((fracpart!=0)&&(m<wei)) //
     {
       fracpart*=10;
       LCD_write_char((int)fracpart+48);
       fracpart-=(int)fracpart;
       m++;
     }    
   }   
  }
  lcd_cs1=1;
}





